<h1>Politique d&#039;accès</h1>
<div class="page">
	<p>&nbsp;</p>
	<p>
		Conjointement à sa publication papier annuelle, les <em>Cahiers
			Ferdinand de Saussure</em> sont&nbsp;désormais diffusée en ligne sur
		le portail de revues de la Librairie Droz avec une barrière mobile de
		3 ans.
	</p>
	<p>Sauf exception, l'accès en PDF et HTML aux articles publiés au cours
		des trois dernières années est réservé aux seuls abonnés à la version
		numérique de la revue, après identification préalable et connexion au
		site de la revue. À l'issue de cette période d'embargo, les articles
		basculent en libre accès et sont rendus consultables en texte intégral
		sur le site de la revue.</p>
	<p>Les particuliers non-abonnés qui souhaitent consulter un article non
		encore disponible en libre accès sont invités à en faire l'acquisition
		au format PDF en téléchargement payant depuis le site de la Librairie
		Droz.</p>
	<p>Tous les comptes rendus de lecture sont en revanche disponibles en
		libre accès direct et consultables hors connexion en HTML.</p>
</div>